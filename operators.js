// Módulo
console.log(`\n6 dividido por 2 tem resto: ${6 % 2}\n`)

a = 10

// Incremento (++)
console.log(`a vale ${a} e foi incrementado em 1. Agora vale: ${++a}`)
console.log(`a vale ${a++} e será incrementado em 1`)
console.log(`   ... então passou a valer: ${a}\n`)

// Decremento (--)
console.log(`a foi decrementado em 1. Agora vale: ${--a}`)
console.log(`a vale ${a--} e será decrementado em 1`)
console.log(`   ... então passou a valer: ${a}\n`)

// Exponenciação
console.log(`2 elevado a 3 resulta em ${2 ** 3}`)
console.log(`10 elevado a -1 resulta em ${10 ** -1}\n`)

// Operador de agrupamento

console.log(`2 * (3 + 2) resulta em ${ 2 * (3 + 2) }\n`)

// Negação e Adição

console.log(`
// Negação e Adição
-----------------------------------------
-3

+"3"    // converte string em number: ${+"3"}

+true   // converte para ${+true}

+false  // converte para ${+false}

-true   // converte para ${-true}
-----------------------------------------
`)